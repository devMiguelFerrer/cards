DROP TABLE IF EXISTS `SL_USER`;
DROP TABLE IF EXISTS `SL_CARD`;
DROP TABLE IF EXISTS `SL_OWNER_STATICS`;
DROP TABLE IF EXISTS `SL_USER_STATICS`;

CREATE TABLE `SL_USER` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `age` int(11) NOT NULL,
  `pass` varchar(255) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE `SL_CARD` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `image` varchar(255) NOT NULL,
  `rarity` varchar(255) NOT NULL,
  `ownerId` int(11) NOT NULL,
  `published` tinyint(4) NOT NULL,
  `type` varchar(255) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=45 DEFAULT CHARSET=latin1;

CREATE TABLE `SL_OWNER_STATICS` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `ownerId` int(11) NOT NULL,
  `cardId` int(11) NOT NULL,
  `cardName` varchar(255) NOT NULL,
  `cardImage` varchar(255) NOT NULL,
  `cardRarity` varchar(255) NOT NULL,
  `cardUses` int(11) NOT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

